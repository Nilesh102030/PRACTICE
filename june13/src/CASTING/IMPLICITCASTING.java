package CASTING;

public class IMPLICITCASTING {
   public static void main(String[] args) {
	byte a=10;              // Byte size is 1 byte lower datatype 
     System.out.println(a); 
	short b=a;              // short size is 2 byte lower datatype
	System.out.println(b);
	double c=b;             // double size is 8 byte lower datatype
	System.out.println(c);
   }	
}
